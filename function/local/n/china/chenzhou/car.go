func normalizePlate(s string) string {
	s = strings.TrimSpace(s)
	s = strings.ReplaceAll(s, " ", "")
	s = strings.ReplaceAll(s, "·", "")
	return s
}

// extractPrefix returns province abbreviation (one Hanzi) and issuing letter A–Z from plate string.
func extractPrefix(plate string) (prov string, letter rune, ok bool) {
	rs := []rune(normalizePlate(plate))
	if len(rs) < 2 {
		return "", 0, false
	}
	prov = string(rs[0])
	if _, known := provinceFullName[prov]; !known {
		return "", 0, false
	}
	for i := 1; i < len(rs); i++ {
		r := rs[i]
		if r >= 'a' && r <= 'z' {
			r = unicode.ToUpper(r)
		}
		if r >= 'A' && r <= 'Z' {
			return prov, r, true
		}
	}
	return "", 0, false
}

// PlateRegion 根据普通小型汽车号牌前部「省简称 + 发牌机关代号（字母）」解析所属省级行政区全称与对应城市/地区说明。
// plate 可含空格或间隔号，如 "湘 L·12345"；新能源号牌取首字母为机关代号（如 "京A" 自 "京AD12345"）。
// 若格式不合法或字母不在对照表中，ok 为 false。
func PlateRegion(plate string) (province, city string, ok bool) {
	prov, letter, ok := extractPrefix(plate)
	if !ok {
		return "", "", false
	}
	pn, okP := provinceFullName[prov]
	if !okP {
		return "", "", false
	}
	key := prov + string(letter)
	cityDesc, okC := plateCity[key]
	if !okC {
		return pn, "", false
	}
	return pn, cityDesc, true
}
